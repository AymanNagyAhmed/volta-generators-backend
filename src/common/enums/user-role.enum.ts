
export enum UserRole {
    /**
     * Regular user with basic privileges
     */
    USER = 'user',
  
    /**
     * Administrator with full system access
     */
    ADMIN = 'admin',
  
    /**
     * Moderator with elevated privileges
     */
    MODERATOR = 'moderator',
  
    /**
     * Staff member with specific operational access
     */
    STAFF = 'staff'
  }